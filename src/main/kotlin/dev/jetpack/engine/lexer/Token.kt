package dev.jetpack.engine.lexer

enum class TokenType {
    INT_LITERAL, FLOAT_LITERAL, STRING_LITERAL, BOOL_LITERAL,
    IDENTIFIER,
    KW_INT, KW_FLOAT, KW_STRING, KW_BOOL, KW_LIST, KW_OBJECT, KW_VAR, KW_NULL,
    KW_FUNCTION, KW_RETURN, KW_CONST,
    KW_PUBLIC, KW_PRIVATE, KW_PROTECTED,
    KW_IF, KW_ELSE, KW_WHILE, KW_FOREACH, KW_IN, KW_BREAK, KW_CONTINUE,
    KW_TRY, KW_CATCH, KW_FINALLY,
    KW_THREAD,
    KW_TO, KW_UNTIL,
    KW_INTERVAL, KW_LISTENER,
    KW_COMMAND, KW_DEFAULT,
    KW_USING,
    KW_AS,
    KW_MANIFEST,
    AT,
    PLUS, MINUS, STAR, SLASH, PERCENT, STAR_STAR,
    PLUS_ASSIGN, MINUS_ASSIGN, STAR_ASSIGN, SLASH_ASSIGN, PERCENT_ASSIGN, STAR_STAR_ASSIGN,
    PLUS_PLUS, MINUS_MINUS,
    BANG, BANG_EQ,
    EQ, EQ_EQ,
    LT, LT_EQ, GT, GT_EQ,
    AMP_AMP, PIPE_PIPE,
    QUESTION, COLON,
    DOT, COMMA, SEMICOLON,
    LPAREN, RPAREN,
    LBRACE, RBRACE,
    LBRACKET, RBRACKET,
    INTERP_STRING,
    NEWLINE,
    EOF,
}

data class Token(
    val type: TokenType,
    val value: String,
    val line: Int,
)
